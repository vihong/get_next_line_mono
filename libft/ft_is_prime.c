#include "libft.h"

int		ft_is_prime(int nb)
{
	int	n;

	n = 2;
	if (nb <= 2 || nb % 2 == 0)
	{
		if (nb == 2)
			return (1);
		else
			return (0);
	}
	while (n <= nb - 1)
	{
		if (nb % n == 0)
			return (0);
		n++;
	}
	return (1);
}
