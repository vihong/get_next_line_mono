#include "libft.h"

char	*ft_strrev(char *str)
{
	int	i;
	int len;

	i = 0;
	len = 0;
	while (str[len] != '\0')
		len++;
	while (i < (len / 2))
	{
		ft_swap((str + i), (str + (len - 1 - i)));
		i++;
	}
	return (str);
}
